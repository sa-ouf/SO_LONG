#include "ft_printf.h"

int	print_unbre(unsigned int nbr)
{
	int	i;

	i = 0;
	if (nbr > 9)
		i += print_unbre(nbr / 10);
	i += print_char(nbr % 10 + 48);
	return (i);
}
