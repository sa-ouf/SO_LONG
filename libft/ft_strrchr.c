#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*d;

	d = NULL;
	while (*s)
	{
		if (*s == (char )c)
			d = (char *)s;
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (d);
}
