#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	x;
	char	*ptr;

	i = 0;
	if (!s1 || !s2)
		return (NULL);
	x = ft_strlen(s1) + ft_strlen(s2) + 1;
	ptr = (char *)malloc(x * sizeof(char));
	if (!ptr)
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	j = 0;
	while (s2[j])
	{
		ptr[i + j] = s2[j];
		j++;
	}
	ptr[i + j] = '\0';
	return (ptr);
}
